RegExp _phone = RegExp(r'(^(?:[+0]9)?[0-9]{8,12}$)');

RegExp _number = RegExp(r'(^[0-9]{1,45}$)');


///ValidatorUtil: This class is used to validate the user inputs.
class ValidatorUtil {

  /// check if the string is an email
  static bool isEmail(String str) {
    return _email.hasMatch(str.toLowerCase());
  }

  /// check if the string is a phone
  static bool isPhone(String str) {
    return !_phone.hasMatch(str);
  }

  /// check if the string is a number
  static bool isNumber(String str) {
    return _number.hasMatch(str);
  }
}
